package bean;

public class User {
  private String login;
  private String passwd;
  
  public User() {}

public String getLogin() {
	return login;
}

public void setLogin(String login) {
	this.login = login;
}

public String getPasswd() {
	return passwd;
}

public void setPasswd(String passwd) {
	this.passwd = passwd;
}
 
}
